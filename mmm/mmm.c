/**
 * Free up memory allocated to all matrices
 */
void mmm_freeup()
{
	// free each row
	for (int i = 0; i < matdim; i++)
	{
		// Free ith row
		free(mat1[i]);
		free(mat2[i]);
		free(mat3[i]);
		// remove dangling pointers
		mat1[i] = NULL;
		mat2[i] = NULL;
		mat3[i] = NULL;
	}
	// free original arrays
	free(mat1);
	free(mat2);
	free(mat3);
	// remove dangling pointers
	mat1 = NULL;
	mat2 = NULL;
	mat2 = NULL;
	return;
}

